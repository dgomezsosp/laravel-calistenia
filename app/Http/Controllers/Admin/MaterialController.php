<?php

namespace App\Http\Controllers\Admin;

use Illuminate\Support\Facades\View;
use App\Http\Controllers\Controller;
use App\Models\Material;
use Illuminate\Http\Request;
use App\Http\Requests\Admin\MaterialRequest;

class MaterialController extends Controller
{
  public function __construct(private Material $material){}
 
  public function index()
  {
    try{
      $records = $this->material
        ->orderBy('created_at', 'desc')
        ->paginate(10);

      $view = View::make('admin.materials.index')
         ->with('records', $records);

      return $view;
    }
    catch(\Exception $e){
     
    }
  }

  public function create()
  {
   try {
      if (request()->ajax()) {
        return response()->json([
        ], 200);
      }
    } catch (\Exception $e) {
      return response()->json([
        'message' =>  \Lang::get('admin/notification.error'),
      ], 500);
    }
  }

  public function store(MaterialRequest $request)
  {  
    try{

     $data = $request->validated();

      $this->material->updateOrCreate([
        'id' => $request->input('id')
      ], $data);

      return response()->json([
        'message' =>  $request->input('id') ? \Lang::get('admin/notification.updated') : \Lang::get('admin/notification.created'),
      ], 201);
    }catch(\Exception $e){
      return response()->json([
        'error' => $e->getMessage(),
      ], 422);
    }    
  }

  public function edit(Material $material)
  {
    return response()->json([
      'element' => $material,
    ], 200);
  }

  public function destroy(Material $material)
  {
    try{
      $material->delete();
     
      return response()->json([
        'message' =>  \Lang::get('admin/notification.deleted'),
      ], 200);
    }catch(\Exception $e){
      return response()->json([
        'error' => $e->getMessage(),
      ], 500);
    }
  }
}